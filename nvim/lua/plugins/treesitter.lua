        configs.setup({
            ensure_installed = { 'html', 'lua', 'vim', 'vimdoc', 'go', 'zig', 'markdown', 'vimdoc', 'bash', 'markdown_inline' },
            sync_install = false,
            highlight = { enable = true },
            indent = { enable = true },
            incremental_selection = {
                enable = true,
                keymaps = {
                    init_selection = "gnn",
                    node_incremental = "grn",
                    scope_incremental = "grc",
                    node_decremental = "grm",
